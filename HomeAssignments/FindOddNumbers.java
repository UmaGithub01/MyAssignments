package week1.HomeAssignments;

public class FindOddNumbers {
public static void main(String[] args) {
	//Input maximum range
	int a=10;
	//For loop to find the odd numbers
	for (int i = 0; i <= a; i++) {
		//Condition check for remainder
		if (i%2==1) {
			System.out.println(i);
			
		}
		
	} 
}
}
